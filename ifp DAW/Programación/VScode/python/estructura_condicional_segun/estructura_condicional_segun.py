
num = int(input("Introduce un numero: "))

if (num==1):
    print("El numero es 1")

if (num==2):
    print("El numero es 2")

if (num==3):
    print("El numero es 3")

if (num!=1 and num!=2 and num!=3):
    print("El numero no es 1, ni 2, ni 3")

